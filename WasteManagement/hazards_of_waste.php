            </header>
            <p>One of the mostly widely known and publicized landfill sites is that of Love Canal in New York State. Large quantities of toxic materials, including residues from pesticides production, were deposited in the 1930s and 1940s, followed by the building of houses and a school on and around the landfill in the 1950s. By the mid 1970s, chemicals leaking from the site were detected in local streams, sewers, soil and indoor air of houses. This site and the subsequent studies of the health of the population in the vicinity fuelled public opinion on the problems of waste disposal practices and raised public concern more generally.</p>
