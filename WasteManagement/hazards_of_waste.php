              <h3>Birth defects and reproductive disorders</h3>
            </header>
            <p> Reproductive effects associated with landfill sites have been extensively researched and include low birth weight (less than 2500 g), fetal and infant mortality, spontaneous abortion, and the occurrence of birth defects. Vianna and Polan13 and Goldman et al14 both found increased incidence of low birth weight in the populations around the Love Canal site, the former during the period of active dumping (1940–1953) and the latter among house owners (although not among those renting) from 1965 to 1978. A similar increase in the proportion of low birth weight babies was found in those living within a radius of 1 km of the Lipari Landfill in New Jersey, particularly in 1971–75 following a period of heavy pollution of streams and a nearby lake from leachate from the site. </p>
            <p>Trends in low birth weight and neonatal deaths were found to correspond closely with time and quantities of dumping at a large hazardous waste disposal site in California, with significantly lower birth weights in exposed areas than control areas during the periods of heaviest dumping16. It should be noted that exposed areas were defined according to the number of odour complaints rather than any more objective measure.</p> 
